import os
import json
from pathlib import Path


def generate_test_html(set_name, data):
    from .sets_utils import load_set_modes, sanitize_filename
    set_modes = load_set_modes()
    if "test" in set_modes and set_name not in set_modes["test"]:
        print(f"‚è≠Ô∏è Skipping test for '{set_name}' (not in test mode).")
        return None

    # ‚úÖ Ensure output dir exists (docs/test/<set_name>/index.html)
    output_dir = Path("docs/test") / set_name
    output_dir.mkdir(parents=True, exist_ok=True)
    test_path = output_dir / "index.html"

    for idx, entry in enumerate(data):
        filename = f"{idx}_{sanitize_filename(entry['phrase'])}.mp3"
        entry["audio_file"] = f"/static/{set_name}/audio/{filename}"

    cards_json = json.dumps(data, ensure_ascii=False)

    test_html = f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8" />
    <title>{set_name} ‚Äì Test Yourself</title>
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <style>
        body {{
            font-family: -apple-system, BlinkMacSystemFont, sans-serif;
            background-color: #f9f9f9;
            padding: 2rem;
            text-align: center;
        }}
        h1 {{
            font-size: 1.6rem;
            margin-bottom: 1rem;
        }}
        .info {{
            font-size: 1rem;
            color: #666;
            margin-bottom: 2rem;
        }}
        .back {{
            display: block;
            margin-top: 2rem;
            font-size: 0.9rem;
            color: #555;
            text-decoration: none;
        }}
    </style>
</head>
<body>
    <h1>üß™ Test Yourself ‚Äì {set_name}</h1>
    <div class="info">Test your memory by matching meanings, pronunciations, and audio.</div>
    <a class="back" href="index.html">‚Üê Back to Mode Selection</a>

    <script>
        const cards = {cards_json};
        const setName = "{set_name}";
    </script>

    <!-- Add your test quiz/game logic here -->

</body>
</html>
"""


    # Write to file
    with open(test_path, "w", encoding="utf-8") as f:
        f.write(test_html)

    print(f"‚úÖ test/index.html generated for: {set_name}")
    return test_path